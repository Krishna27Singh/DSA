/*
****************************************** APPROACH **************************************************
minimum integer k : k^n % n == 0
tc -> < O(N)
*/

void solve(){
    ll n; cin>>n;
    
    ll temp = n;
    ll k = 1;
    for(ll i = 2; i*i<= temp; i++){
        if(temp%i == 0){
            k *= i;
            while(temp%i == 0) temp /= i;
        }
    }

    if(temp > 1) k *= temp;
    cout<<k<<endl;

}

