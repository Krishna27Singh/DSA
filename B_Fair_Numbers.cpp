bool checkFair(ll n){
    ll orgN = n;
    while(n){
        int digit = n % 10;
        if(digit != 0 && orgN % digit != 0){
            return false;
        }
        n /= 10;
    }
    return true;
}

void solve(){
    ll n; cin>>n;

    while(!checkFair(n))
        n++;

    cout<<n<<endl;
}

