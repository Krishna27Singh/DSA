void solve(){
    int n, x, k;
    cin >> n >> x >> k;

    int mini = k * (k + 1) / 2;
    int maxi = k * (2 * n - k + 1) / 2;

    if (x >= mini && x <= maxi)
        cout << "YES\n";
    else
        cout << "NO\n";
}


