void solve(){
    int n; cin>>n;
    if(n%2!=0){
        cout<<0<<endl;
        return;
    }
    int count = 1;
    for(int x = 0; x<n/2; x++){
        if((n-2*x)%4 == 0) count++;
    }

    cout<<count<<endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    int tc; cin >> tc;
    while (tc--) solve();
    return 0;
}
